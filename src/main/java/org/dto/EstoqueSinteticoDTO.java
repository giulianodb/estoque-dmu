package org.dto;

public class EstoqueSinteticoDTO {
	private Integer idProduto;
	private String nomeProduto;
	private String unidadeMedida;
	private String local;
	private String quantidadeSaldoAnterior;
	private String valorSaldoAnterior;
	
	private String quantidadeEntrada;
	private String valorEntrada;
	
	private String quantidadeSaida;
	private String valorSaida;
	
	private String quantidadeSaldoAtual;
	private String valorSaldoAtual;
	private String valorMedioAtual;
	public Integer getIdProduto() {
		return idProduto;
	}
	public void setIdProduto(Integer idProduto) {
		this.idProduto = idProduto;
	}
	public String getNomeProduto() {
		return nomeProduto;
	}
	public void setNomeProduto(String nomeProduto) {
		this.nomeProduto = nomeProduto;
	}
	public String getUnidadeMedida() {
		return unidadeMedida;
	}
	public void setUnidadeMedida(String unidadeMedida) {
		this.unidadeMedida = unidadeMedida;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	public String getQuantidadeSaldoAnterior() {
		return quantidadeSaldoAnterior;
	}
	public void setQuantidadeSaldoAnterior(String quantidadeSaldoAnterior) {
		this.quantidadeSaldoAnterior = quantidadeSaldoAnterior;
	}
	public String getValorSaldoAnterior() {
		return valorSaldoAnterior;
	}
	public void setValorSaldoAnterior(String valorSaldoAnterior) {
		this.valorSaldoAnterior = valorSaldoAnterior;
	}
	public String getQuantidadeEntrada() {
		return quantidadeEntrada;
	}
	public void setQuantidadeEntrada(String quantidadeEntrada) {
		this.quantidadeEntrada = quantidadeEntrada;
	}
	public String getValorEntrada() {
		return valorEntrada;
	}
	public void setValorEntrada(String valorEntrada) {
		this.valorEntrada = valorEntrada;
	}
	public String getQuantidadeSaida() {
		return quantidadeSaida;
	}
	public void setQuantidadeSaida(String quantidadeSaida) {
		this.quantidadeSaida = quantidadeSaida;
	}
	public String getValorSaida() {
		return valorSaida;
	}
	public void setValorSaida(String valorSaida) {
		this.valorSaida = valorSaida;
	}
	public String getQuantidadeSaldoAtual() {
		return quantidadeSaldoAtual;
	}
	public void setQuantidadeSaldoAtual(String quantidadeSaldoAtual) {
		this.quantidadeSaldoAtual = quantidadeSaldoAtual;
	}
	public String getValorSaldoAtual() {
		return valorSaldoAtual;
	}
	public void setValorSaldoAtual(String valorSaldoAtual) {
		this.valorSaldoAtual = valorSaldoAtual;
	}
	public String getValorMedioAtual() {
		return valorMedioAtual;
	}
	public void setValorMedioAtual(String valorMedioAtual) {
		this.valorMedioAtual = valorMedioAtual;
	}
	
	
}
